import os
import httpx
import logging

logger = logging.getLogger(__name__)

RESEND_API_KEY = os.getenv("RESEND_API_KEY")
ADMIN_EMAIL = os.getenv("ADMIN_EMAIL", "growix299@gmail.com")

async def send_delivery_email(to_email: str, product_name: str, order_id: str) -> bool:
    """
    Sends a confirmation email to the user when they buy an OTT product.
    Since domain verification is pending, it sends via onboarding@resend.dev.
    """
    if not RESEND_API_KEY:
        logger.error("RESEND_API_KEY environment variable is not set.")
        return False

    url = "https://api.resend.com/emails"
    headers = {
        "Authorization": f"Bearer {RESEND_API_KEY}",
        "Content-Type": "application/json"
    }

    # Now using the verified custom domain for sending emails to any customer
    payload = {
        "from": "support@nexatutomate.shop",
        "to": to_email,
        "subject": f"OTT Activation Request - {product_name}",
        "html": f"""
        <div style="font-family: Arial, sans-serif; max-width: 600px; margin: auto; padding: 20px; border: 1px solid #ddd; border-radius: 8px; background-color: #0d0e12; color: #fff;">
            <div style="text-align: center; margin-bottom: 20px;">
                <h1 style="color: #38bdf8; font-weight: bold; font-family: 'Poppins', sans-serif;">OTT ACTIVATION IN PROGRESS</h1>
                <p style="color: #9ca3af; font-size: 14px;">Order ID: {order_id}</p>
            </div>
            <div style="background-color: #1e293b; padding: 15px; border-radius: 6px; margin-bottom: 20px;">
                <h3 style="margin-top: 0; color: #fff;">Order Details:</h3>
                <p style="margin: 5px 0;"><strong>Product:</strong> {product_name}</p>
                <p style="margin: 5px 0;"><strong>Status:</strong> Processing</p>
                <p style="margin: 5px 0;"><strong>Delivery Type:</strong> Manual Activation</p>
            </div>
            <p style="font-size: 15px; line-height: 1.5; color: #d1d5db;">
                Thank you for your purchase! Our team is activating your <strong>{product_name}</strong> account. 
                You will receive another update on Telegram or via email as soon as it is active.
            </p>
            <hr style="border-color: #334155; margin: 20px 0;">
            <p style="font-size: 12px; color: #6b7280; text-align: center;">
                This email was automatically generated by TG Bot Delivery System.
            </p>
        </div>
        """
    }

    try:
        async with httpx.AsyncClient() as client:
            response = await client.post(url, headers=headers, json=payload, timeout=10.0)
            if response.status_code in (200, 201):
                logger.info(f"Email sent successfully to {to_email}")
                return True
            else:
                logger.error(f"Failed to send email. Status code: {response.status_code}, Response: {response.text}")
                return False
    except Exception as e:
        logger.error(f"Exception raised while sending email: {str(e)}")
        return False


